#include "libft.h"

void	ft_putchar_fd(char c, int fd)
{
	write(fd, &c, 1);
}
// #1. The character to output.
// #2. The file descriptor on which to write.
// None
// write
// Outputs the character ’c’ to the given file
// descriptor.

