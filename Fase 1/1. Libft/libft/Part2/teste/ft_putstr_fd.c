#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	while (*s)
	{
		write(fd, s, 1);
		s++;
	}
}
// #1. The string to output.
// #2. The file descriptor on which to write.
// None
// write
// Outputs the string ’s’ to the given file
// descriptor.

